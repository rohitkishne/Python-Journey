# Operators - symbol or word do operation on values or variables
# Types
# 1) Arithmetic Operator
# 2) Comparison Operator
# 3) Assignment Operator
# 4) Logical Operator
# 5) Identify Operator
# 6) Membership Operator
# 7) Bitwise Operator

# 1) Arithmetic Operator -
# Add, Subtraction, Multiplication, Division, 
# floor Division (//) , modulus Remainder (%), Exponent Power(**) 

# 2) Comparison Operator - ==, !=, >, <, >=, <=

# 3) Assignment Operator - =, +=, -=, *=, /= (Assign and update values)

# 4) Logical Operator - combine mutiple condition
# AND, OR, NOT

# 5) Identify Operator - pointing to same memory location or not
# is - same object then true
# is not - different object then true
# m1 = 100
# m2 = 100
# print(m1 is m2)

# 6) Membership Operator - check value is exist inside a sequence(list, string, tuple)
# in, not in
# print("check e in mango :",'e' not in 'mango')

# 7) Bitwise Operator - binary operations
# & - AND, 
# ` - `, 
# ^ - XOR, 
# ~ - NOT, 
# << - Left Shift, 
# >> - Right Shift

