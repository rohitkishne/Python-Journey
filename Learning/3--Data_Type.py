# Seven types of Data types
# 1) Numeric Type - INT, FLOAT, COMPLEX
# 2) Text Type- str
# 3) Sequence Type - List, Tupple, range
# 4) Boolean Type - bool
# 5) Set Type - Set, frozenset
# 6) Mapping Type - Dict
# 7) None datatype

# # 1) Numeric Type - INT, FLOAT, COMPLEX
# # Int - whole number
# num_int = 1000
# print(num_int)

# # float - point value
# num_floating = 1000.00
# print(num_floating)

# # Complex - imaginary number (a+ib)
# num_complex = 4 + 3j
# print(num_complex)

# # 2) Text Type- str
# string - alphabet letters
# name = "shyam"
# print(name)

# 3) Sequence Type - List, Tupple, range
# List - mutable
# Fruit = ["apple","banana","mango"]
# print(Fruit)

# Tupple - immutable
# Year = (1980,1950,1960)
# print(Year)

# Range - indexing
# count = range(5)
# print(list(count))
# print(tuple(count))

# 4) Boolean Type - bool
# bool - True/False
# is_Working = True
# print(is_Working)

# 5) Set Type - Set, frozenset
# set - mutable
# rank = {1,2,2,2,3,4,5,6}
# print(rank)

# frozenset - immutable
# rank_immutable = ([1,2,3,4,4,5])
# print(rank_immutable)

# 6) Mapping Type - Dict
# dictionary - Key:value pair
# Student = {
#   "name" : "Shubham",
#   "age" : 24
# }
# print(Student)

# 7) None datatype
# None - empty and no value
# Response = None
# print(Response)
